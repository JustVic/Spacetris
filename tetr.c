void next_tetr(int num_tetr, int num_rotation, int tetr[5][5])
{
	int i,j;

	for(i = 0; i<5; i++)
	{
		for(j =0; j<5; j++)
		{
			tetr[i][j]=tetrs[num_tetr][num_rotation][i][j];
		}
	}
}



